export type TranslationKey = keyof typeof translations.en;

export const tText = (language: Language, key: TranslationKey, vars?: Record<string, string>) => {
  let text = translations[language][key] ?? translations.en[key];
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
};

export const getResourceTypeLabel = (language: Language, type: ResourceType) => {
  if (type === 'desk') return tText(language, 'desk');
  if (type === 'room') return tText(language, 'room');
  return tText(language, 'parking');
};
